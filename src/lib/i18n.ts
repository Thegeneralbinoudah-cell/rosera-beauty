export function tr(lang: Lang, key: string, vars?: Record<string, string | number>) {
  const base = dict[lang][key] ?? dict.ar[key] ?? key
  if (!vars) return base
  return Object.entries(vars).reduce((out, [k, v]) => out.replaceAll(`{{${k}}}`, String(v)), base)
}
